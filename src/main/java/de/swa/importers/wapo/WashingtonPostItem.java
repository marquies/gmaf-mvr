package de.swa.importers.wapo;

import java.util.HashMap;
import java.util.List;
import java.util.Map;

/** data structure to represent an item of the Washington Post archive **/
public class WashingtonPostItem {
	private String id;
	private String articleUrl;
	private String title;
	private String author;
	private Integer publishedDate;
	private List<Content> contents = null;
	private String type;
	private String source;
	private Map<String, Object> additionalProperties = new HashMap<String, Object>();

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getArticleUrl() {
		return articleUrl;
	}

	public void setArticleUrl(String articleUrl) {
		this.articleUrl = articleUrl;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public Integer getPublishedDate() {
		return publishedDate;
	}

	public void setPublishedDate(Integer publishedDate) {
		this.publishedDate = publishedDate;
	}

	public List<Content> getContents() {
		return contents;
	}

	public void setContents(List<Content> contents) {
		this.contents = contents;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public Map<String, Object> getAdditionalProperties() {
		return this.additionalProperties;
	}

	public void setAdditionalProperty(String name, Object value) {
		this.additionalProperties.put(name, value);
	}

}


